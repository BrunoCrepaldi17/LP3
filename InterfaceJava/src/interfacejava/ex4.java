    private void jButtonBrancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBrancoActionPerformed
        try {
            DecimalFormat df = new DecimalFormat("#,###.00");
            Integer branco = Integer.parseInt(jLabelBrancos.getText());
            Integer total = Integer.parseInt(jLabelTotal.getText());

            jLabelBrancos.setText(String.valueOf(branco + 1));
            jLabelTotal.setText(String.valueOf(total + 1));

            double porcentagem = ((double) (branco + 1) / (total + 1)) * 100;
            String porcentagemFormatada = df.format(porcentagem);

            jLabelPerBrancos.setText(String.valueOf(porcentagemFormatada));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "√â permitido apenas numeros");
        }
    }//GEN-LAST:event_jButtonBrancoActionPerformed

