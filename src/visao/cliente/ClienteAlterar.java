    private void jTextFieldIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldIDFocusLost
        String id = jTextFieldID.getText();

        if (!id.trim().equals("")) {
            this.mostrarUsuario(id);
        } else {
            jTextFieldID.setText("");
            jTextFieldNome.setText("");
            txtCpfCnpj.setText("");
            cbxClientType.setSelectedIndex(0);
            txtEmail.setText("");
            txtObservation.setText("");
            txtPhone.setText("");
        }
    }//GEN-LAST:event_jTextFieldIDFocusLost

    public void mostrarUsuario(String idTexto) {
        try {
            Integer id = Integer.parseInt(idTexto);

            ClienteDao dao = new ClienteDao();
            Cliente obj = dao.getUsuario(id);

            if (obj != null) {
                //Preenche os dados do formulário
                jTextFieldID.setText(obj.getId().toString());
                jTextFieldNome.setText(obj.getNome());
                cbxClientType.setSelectedIndex(obj.getTipocliente() - 1);
                txtCpfCnpj.setText(obj.getCpfcnpj());
                txtPhone.setText(obj.getTelefone());
                txtEmail.setText(obj.getEmail());
                txtObservation.setText(obj.getObservacao());
            } else {
                JOptionPane.showMessageDialog(this, "Registro não encontrado.");
                jTextFieldID.setText("");
                jTextFieldNome.setText("");
                cbxClientType.setSelectedIndex(1);
                txtCpfCnpj.setText("");
                txtPhone.setText("");
                txtEmail.setText("");
                txtObservation.setText("");
                jTextFieldID.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar registro.");
            jTextFieldID.setText("");
            jTextFieldNome.setText("");
            cbxClientType.setSelectedIndex(1);
            txtCpfCnpj.setText("");
            txtPhone.setText("");
            txtEmail.setText("");
            txtObservation.setText("");
            jTextFieldID.requestFocus();
        }
    }

    private void jButtonSalvarDadosGeraisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarDadosGeraisActionPerformed
        //Validações
        // - Campos obrigatórios
        // - Email correto

        Integer id = Integer.valueOf(jTextFieldID.getText());
        String nome = jTextFieldNome.getText();
        Integer tipocliente = cbxClientType.getSelectedIndex() + 1;
        String cpfcnpj = txtCpfCnpj.getText();
        String telefone = txtPhone.getText();
        String email = txtEmail.getText();
        String observacao = txtObservation.getText();

        //Validações
        if (nome.equals("")) {
            JOptionPane.showMessageDialog(this, "Informe o nome.");
            jTextFieldNome.requestFocus();
            return;
        }
        if (cpfcnpj.equals("")) {
            JOptionPane.showMessageDialog(this, "Informe o CPF/CNPJ.");
            txtCpfCnpj.requestFocus();
            return;
        }
        if (telefone.equals("")) {
            JOptionPane.showMessageDialog(this, "Informe o telefone.");
            txtPhone.requestFocus();
            return;
        }
        if (email.equals("")) {
            JOptionPane.showMessageDialog(this, "Informe o email.");
            txtEmail.requestFocus();
            return;
        }
        if (observacao.equals("")) {
            JOptionPane.showMessageDialog(this, "Informe a observação.");
            txtObservation.requestFocus();
            return;
        }
        Cliente u = new Cliente(id, nome, tipocliente, cpfcnpj, telefone, email, observacao);
        try {
            ClienteDao dao = new ClienteDao();
            dao.atualizar(u);

            ClienteGerenciarForm.callback();

            JOptionPane.showMessageDialog(this, "Registro atualizado com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonSalvarDadosGeraisActionPerformed

